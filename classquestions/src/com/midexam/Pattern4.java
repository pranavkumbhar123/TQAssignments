package com.midexam;

public class Pattern4 {

	public static void main(String[] args) {


		for(char ch='A';ch<='E';ch++)
		{
			for(char j=ch;j<='E';j++)
			{
				System.out.print(j+" ");
			}
			System.out.println();
		}

	
	
	for(char ch='A';ch<='E';ch++)
	{
		for(char j='A';j<=ch;j++)
		{
			System.out.print(j+" ");
		}
		System.out.println();
	}

}
}
